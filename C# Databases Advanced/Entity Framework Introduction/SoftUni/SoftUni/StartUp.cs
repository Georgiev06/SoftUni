        public static string DeleteProjectById(SoftUniContext context)
        {
            var sb = new StringBuilder();

            var ep = context.EmployeesProjects
                .Where(ep => ep.ProjectId == 2);

            context.EmployeesProjects.RemoveRange(ep);

            var projectToDelete = context.Projects.Find(2);
            context.Projects.Remove(projectToDelete);
            context.SaveChanges();

            var projectNames = context.Projects
                .Take(10)
                .Select(p => p.Name)
                .ToList();

            foreach (var projectName in projectNames)
            {
                sb.AppendLine($"{projectName}");
            }

            return sb.ToString().TrimEnd();
        }

        public static string RemoveTown(SoftUniContext context)
        {
            var townToDelete = context.Towns
                .FirstOrDefault(t => t.Name == "Seattle");

            var referencedAddresses = context
                .Addresses
                .Where(a => a.TownId == townToDelete.TownId)
                .ToArray();

            foreach (var e in context.Employees)
            {
                if (referencedAddresses.Any(a => a.AddressId == e.AddressId))
                {
                    e.AddressId = null;
                }
            }

            var numberOfDeletedAddresses = referencedAddresses.Length;

            context.Addresses.RemoveRange(referencedAddresses);
            context.Towns.Remove(townToDelete);

            context.SaveChanges();

            return $"{numberOfDeletedAddresses} addresses in Seattle were deleted";
        }
